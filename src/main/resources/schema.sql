DROP TABLE IF EXISTS film_genres CASCADE;
DROP TABLE IF EXISTS film_likes CASCADE;
DROP TABLE IF EXISTS films CASCADE;
DROP TABLE IF EXISTS Friendship CASCADE;
DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS mpa CASCADE;
DROP TABLE IF EXISTS genres CASCADE;

CREATE TABLE IF NOT EXISTS mpa(
id       BIGINT  GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
mpa_name VARCHAR(100));

CREATE TABLE IF NOT EXISTS films(
id           BIGINT         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
films_name   VARCHAR(255)   NOT NULL,
description  VARCHAR(200)   NOT NULL,
release_date DATE           NOT NULL,
duration     INTEGER        NOT NULL,
mpa_id       BIGINT,
CONSTRAINT films_mpa_fk FOREIGN KEY(mpa_id) REFERENCES mpa(id));

CREATE TABLE IF NOT EXISTS genres(
id         BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
genre_name VARCHAR(100) NOT NULL);

CREATE TABLE IF NOT EXISTS FILM_GENRES(
id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
film_id  BIGINT NOT NULL,
genre_id BIGINT NOT NULL,
-- CONSTRAINT film_genres_unique UNIQUE (film_id, genre_id),
CONSTRAINT FILM_GENRES_FILMS_FILM_ID_fk FOREIGN KEY (film_id) REFERENCES FILMS(id),
CONSTRAINT FILM_GENRES_GENRES_GENRE_ID_fk FOREIGN KEY (genre_id) REFERENCES GENRES(id));

CREATE TABLE IF NOT EXISTS users(
id        BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
user_name VARCHAR(100) NOT NULL,
email     VARCHAR(255) NOT NULL,
login     VARCHAR(100) NOT NULL,
birthday  DATE         NOT NULL);

CREATE TABLE IF NOT EXISTS Friendship(
id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
user_id   BIGINT NOT NULL,
friend_id BIGINT NOT NULL,
CONSTRAINT Friends_unique UNIQUE (user_id, friend_id),
CONSTRAINT Friends_USERS_USER_ID_fk FOREIGN KEY (user_id) REFERENCES USERS(id),
CONSTRAINT Friends_USERS_USER_ID_fk_2 FOREIGN KEY (friend_id) REFERENCES USERS(id));

CREATE TABLE IF NOT EXISTS Film_likes(
id      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
film_id BIGINT NOT NULL,
user_id BIGINT NOT NULL,
CONSTRAINT Film_likes_unique UNIQUE (film_id, user_id),
CONSTRAINT Film_likes_FILMS_FILM_ID_fk FOREIGN KEY (film_id) REFERENCES FILMS(id),
CONSTRAINT Film_likes_USERS_USER_ID_fk FOREIGN KEY (user_id) REFERENCES USERS(id));